# isspace() istitle() isupper() islower() isalpha() isalnum() isdecimal() isdigit() isnumeric()

# isspace() 检查字符串是否为空或仅包含空白字符

# istitle() 检查字符串中所有的单词拼写首字母是否为大写，且其他字母为小写

# isupper() 检查字符串中所有的字母是否都为大写

# islower() 检查字符串中所有的字母是否都为小写

# isalpha() 检查字符串是否只包含字母

# isalnum() 检查字符串是否只包含字母和数字

# isdecimal() 检查字符串是否只包含十进制字符

# isdigit() 检查字符串是否只包含数字

# isnumeric() 检查字符串是否只包含数字字符

# 以上方法都不接受任何参数 并且返回bool
